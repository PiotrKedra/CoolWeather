
function getLightTheme(){
    return {
        mainColor: '#ddd',
        softColor: '#ccc',
        mainText: '#111',
        softText: '#777',
    }
}

function getDarkTheme(){
    return {
        mainColor: '#222',
        softColor: '#444',
        mainText: '#bbb',
        softText: '#777',
    }
}

export {getLightTheme, getDarkTheme};
